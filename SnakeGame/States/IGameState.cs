using SFML.Graphics;

namespace SnakeGame.States;
// Интерфейс, определяющий общий контракт для всех состояний игры
// Реализация паттерна State позволяет легко добавлять новые экраны
public interface IGameState
{
    // Инициализирует состояние (создает UI элементы, загружает ресурсы)
    void Initialize();
    // Обрабатывает пользовательский ввод (нажатия клавиш)
    void HandleInput();
    // Обновляет логику состояния (вызывается каждый кадр)
    // deltaTime - время, прошедшее с предыдущего кадра (в секундах)
    void Update(float deltaTime);
    // Отрисовывает состояние на экране
    // target - цель отрисовки (обычно окно игры)
    void Draw(RenderTarget target);
}
